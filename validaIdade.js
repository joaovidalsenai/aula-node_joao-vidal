let idade = 18

if (idade >= 0 && idade < 7) {
    console.log("Infância");
} else if (idade >= 7 && idade < 10) {
    console.log("Segunda Infância");
} else if (idade >= 10 && idade < 15) {
    console.log("Pré-Adolescência");
} else if (idade >= 15 && idade < 20) {
    console.log("Adolescência");
} else if (idade >= 20 && idade < 30) {
    console.log("Juventude");
} else if (idade >= 30 && idade <= 60) {
    console.log("Adulta");
} else {
    console.log("Faixa etária não classificada");
}
  