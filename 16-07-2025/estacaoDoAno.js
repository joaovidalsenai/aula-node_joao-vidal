let numero = 2;

if (numero === 1) {
  console.log("Verão");
} else if (numero === 2) {
  console.log("Outono");
} else if (numero === 3) {
  console.log("Inverno");
} else if (numero === 4) {
  console.log("Primavera");
} else {
  console.log("Número inválido! Por favor, digite um número entre 1 e 4.");
}
